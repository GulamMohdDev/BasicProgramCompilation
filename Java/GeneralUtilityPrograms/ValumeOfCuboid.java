//Program to calculate the volume of a cuboid given its length, width, and height
public class ValumeOfCuboid {
    public static void main(String[] args) {
        double length = 5.0; // You can change this value to test other lengths
        double width = 3.0;  // You can change this value to test other widths
        double height = 4.0; // You can change this value to test other heights
        double volume = length * width * height;
        System.out.println("Volume of cuboid with length " + length + ", width " + width + " and height " + height + " is: " + volume);
    }
}
