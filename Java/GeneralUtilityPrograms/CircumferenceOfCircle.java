//Program to calculate the circumference of a circle given its radius
public class CircumferenceOfCircle {
    public static void main(String[] args) {
        double radius = 7.0; // You can change this value to test other radii
        double circumference = 2 * Math.PI * radius;
        System.out.println("Circumference of circle with radius " + radius + " is: " + circumference);
    }
}
