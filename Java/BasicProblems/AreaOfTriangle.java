//Program to calculate the area of a triangle given its base and height
public class AreaOfTriangle {
    public static void main(String[] args) {
        double base = 5.0; // You can change this value to test other bases
        double height = 10.0; // You can change this value to test other heights
        double area = 0.5 * base * height;
        System.out.println("Area of triangle with base " + base + " and height " + height + " is: " + area);
    }
}
